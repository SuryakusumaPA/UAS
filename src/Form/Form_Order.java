    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        try {
            st = connect.createStatement();
            if (txtNama.getText().equals("") ||
                txtAlamat.getText().equals("") ||
                txtTelp.getText().equals("") ||
                txtSeri.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "Data tidak boleh kosong", "Validasi Data", JOptionPane.INFORMATION_MESSAGE);
                return;
            }

            if (btnSimpan.getText() == "Simpan") {
                String cek = "SELECT * FROM data_order WHERE `Nama Lengkap` = '" + txtNama.getText() + "'";
                rs = st.executeQuery(cek);
                if (rs.next()) {
                    JOptionPane.showMessageDialog(null, "Nama pelanggan ini sudah terdaftar");
                } else {
                    String sql = "INSERT INTO data_order VALUES ('" + txtNama.getText() +
                    "','" + txtAlamat.getText() +
                    "','" + txtTelp.getText() +
                    "','" + txtSeri.getText() + "')";
                    st.executeUpdate(sql);
                    JOptionPane.showMessageDialog(null, "Data telah berhasil disimpan");
                    bersihTabel();
                    tampilData();
                }
            } else {
                //Pemicu pengubahan data
                String update = "UPDATE data_order SET `Alamat Rumah` = '" + txtAlamat.getText() +
                "', `Nomor Telepon` = '" + txtTelp.getText() +
                "', `Seri Laptop` = '" + txtSeri.getText() +
                "' WHERE `Nama Lengkap` = '" + txtNama.getText() + "'";
                st.executeUpdate(update);
                JOptionPane.showMessageDialog(null, "Data berhasil disimpan");
                bersihTabel();
                tampilData();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error" + e.getMessage());
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        if (txtNama.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Silahkan pilih data yang akan dihapus!");
        } else {
            int Jawaban = JOptionPane.showConfirmDialog(null, "Data ini akan dihapus, Tekan Yes untuk lanjutkan", "Konfirmasi", JOptionPane.YES_NO_OPTION);
            if (Jawaban == 0) {
                try {
                    st = connect.createStatement();
                    String sql = "DELETE FROM data_order WHERE `Nama Lengkap` = '" + txtNama.getText() + "'";
                    st.executeUpdate(sql);
                    JOptionPane.showMessageDialog(null, "Data berhasil dihapus");
                    tampilData();
                    bersihTabel();
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, "Error");
                }
            }
        }
    }//GEN-LAST:event_btnHapusActionPerformed

