    private void cariData(){
        try {
            st = connect.createStatement();
            rs = st.executeQuery("SELECT * FROM data_order WHERE `" 
                    + comboCari.getSelectedItem().toString() + 
                    "` LIKE '%" + txtCari.getText() + "%'");
            
            DefaultTableModel model = new DefaultTableModel();
            model.addColumn("No.");
            model.addColumn("Nama Lengkap");
            model.addColumn("Alamat Rumah");
            model.addColumn("Nomor Telepon");
            model.addColumn("Seri Laptop");
            
            int no = 1;
            
            model.getDataVector().removeAllElements();
            model.fireTableDataChanged();
            model.setRowCount(0);
            tabelData.setModel(model);
            
            while (rs.next()) {
                Object[] data = {
                    no ++,
                    rs.getString("Nama Lengkap"),
                    rs.getString("Alamat Rumah"),
                    rs.getString("Nomor Telepon"),
                    rs.getString("Seri Laptop")
                        
                };
                model.addRow(data); 
            }
            
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Error" + e.getMessage());
        }
    }
    
